export const createdAt_asc_sort = (a, b) => a.createdAt - b.createdAt;

export const createdAt_desc_sort = (a, b) => b.createdAt - a.createdAt;

export const updatedAt_asc_sort = (a, b) => a.updatedAt - b.updatedAt;

export const updatedAt_desc_sort = (a, b) => b.updatedAt - a.updatedAt;

export const order_asc_sort = (a, b) => a.order - b.order;
