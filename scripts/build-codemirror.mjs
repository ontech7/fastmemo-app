import * as esbuild from "esbuild";
import { writeFileSync } from "fs";

const EDITOR_BG = "#282c34";
const CURSOR_COLOR = "#A3D9C8";

const result = await esbuild.build({
  entryPoints: ["src/editor/codemirror-entry.js"],
  bundle: true,
  format: "iife",
  write: false,
  minify: true,
  target: "es2020",
});

const jsBundle = result.outputFiles[0].text;

const html = `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width,initial-scale=1,maximum-scale=1,user-scalable=no">
<style>
*{margin:0;padding:0;box-sizing:border-box}
html,body{height:100%;background:${EDITOR_BG};overflow:hidden;-webkit-tap-highlight-color:transparent}
#editor{height:100%}
.cm-editor{height:100%;font-size:14px}
.cm-editor .cm-scroller{overflow:auto;-webkit-overflow-scrolling:touch}
.cm-editor .cm-content{padding:12px;min-height:100%;caret-color:${CURSOR_COLOR}}
.cm-editor .cm-gutters{background:#21252b;border-right:1px solid #181a1f;padding-right:8px;position:sticky;left:0;z-index:2}
.cm-editor .cm-lineNumbers .cm-gutterElement{color:#636d83;font-size:12px;min-width:2em;padding:0 4px}
.cm-editor .cm-activeLine{background:rgba(255,255,255,0.04)}
.cm-editor .cm-activeLineGutter{background:rgba(255,255,255,0.04)}
.cm-editor.cm-focused{outline:none}
.cm-editor .cm-cursor{border-left-color:${CURSOR_COLOR};border-left-width:2px}
.cm-editor .cm-selectionBackground{background:rgba(163,217,200,0.15)!important}
.cm-editor.cm-focused .cm-selectionBackground{background:rgba(163,217,200,0.25)!important}
.cm-editor .cm-matchingBracket{background:rgba(163,217,200,0.3);outline:1px solid rgba(163,217,200,0.5)}
</style>
</head>
<body>
<div id="editor"></div>
<script>window.__EDITOR_CONFIG__=window.__EDITOR_CONFIG__||{};</script>
<script>${jsBundle}</script>
</body>
</html>`;

writeFileSync(
  "src/generated/codemirror-html.ts",
  `// Auto-generated by scripts/build-codemirror.mjs — DO NOT EDIT\nexport const CODEMIRROR_HTML = ${JSON.stringify(html)};\n`
);

console.log(
  `Built CodeMirror bundle: ${(jsBundle.length / 1024).toFixed(0)}KB JS, HTML written to src/generated/codemirror-html.ts`
);
